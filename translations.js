function changeLanguage(lang) {
    // –ó–±–µ—Ä—ñ–≥–∞—î–º–æ –≤–∏–±—Ä–∞–Ω—É –º–æ–≤—É
    localStorage.setItem('selectedLanguage', lang);

    // –û–Ω–æ–≤–ª—é—î–º–æ –∞–∫—Ç–∏–≤–Ω—É –∫–Ω–æ–ø–∫—É (–≤ –æ–±–æ—Ö –º—ñ—Å—Ü—è—Ö: –¥–µ—Å–∫—Ç–æ–ø —ñ –º–æ–±—ñ–ª—å–Ω–µ –º–µ–Ω—é)
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });

    // –¢–∞–∫–æ–∂ –æ–Ω–æ–≤–ª—é—î–º–æ –≤ –º–æ–±—ñ–ª—å–Ω–æ–º—É –º–µ–Ω—é —è–∫—â–æ —î
    const mobileLangContainer = document.getElementById('mobile-lang-switcher');
    if (mobileLangContainer) {
        mobileLangContainer.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.dataset.lang === lang) {
                btn.classList.add('active');
            }
        });
    }

    // –û–Ω–æ–≤–ª—é—î–º–æ –∫–æ–Ω—Ç–µ–Ω—Ç
    updateContent(lang);
}

